// Logical Operators

(1 == 1) && (2 == 2) 
// Returns true as both operands are true, so true AND true returns true.
(1 == 1) && (2 != 2) 
// Returns false as one operand is false, so true AND false returns false. 
(1 == 1) || (2 == 2) 
// Returns true as both operands are true, so true OR true returns true. 
(1 == 1) || (2 != 2) 
// Returns true as one operand is true, so true OR false returns true. 
(1 != 1) || (2 != 2) 
// Returns false as both operands are false, so false OR false returns false. 
